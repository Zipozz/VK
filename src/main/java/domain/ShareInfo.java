package domain;

public class ShareInfo {
    private int count;
    private boolean userShare;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public boolean isUserShare() {
        return userShare;
    }

    public void setUserShare(boolean userShare) {
        this.userShare = userShare;
    }
}